import React, { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { useAuth } from '../contexts/AuthContext';
import { Eye, EyeOff, Mail, Lock, User } from 'lucide-react';

const Register: React.FC = () => {
  const [username, setUsername] = useState('');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [error, setError] = useState('');
  const [loading, setLoading] = useState(false);
  
  const { register } = useAuth();
  const navigate = useNavigate();

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');
    setLoading(true);

    console.log('ðŸ”§ Register Debug - Form submission started');
    console.log('ðŸ”§ Register Debug - Form data:', { username, email, password: '***' });

    try {
      console.log('ðŸ”§ Register Debug - Calling register function...');
      await register(username, email, password);
      console.log('ðŸ”§ Register Debug - Registration successful, navigating to dashboard');
      navigate('/dashboard');
    } catch (err) {
      console.error('ðŸ”§ Register Debug - Registration error:', err);
      
      // Enhanced error handling
      let errorMessage = 'Failed to register';
      
      if (err instanceof Error) {
        errorMessage = err.message;
        
        // Check for specific network errors
        if (err.message.includes('Network Error')) {
          errorMessage = 'Network error: Unable to connect to the server. Please check if the server is running.';
        } else if (err.message.includes('timeout')) {
          errorMessage = 'Request timeout: The server took too long to respond.';
        } else if (err.message.includes('ECONNREFUSED')) {
          errorMessage = 'Connection refused: The server is not running or not accessible.';
        }
      } else if (typeof err === 'object' && err !== null) {
        // Handle axios error objects
        const axiosError = err as any;
        if (axiosError.response) {
          // Server responded with error status
          errorMessage = `Server error: ${axiosError.response.status} - ${axiosError.response.data?.error || axiosError.response.statusText}`;
        } else if (axiosError.request) {
          // Request was made but no response received
          errorMessage = 'No response from server: Please check your internet connection and try again.';
        } else {
          // Something else happened
          errorMessage = `Request error: ${axiosError.message}`;
        }
      }
      
      console.log('ðŸ”§ Register Debug - Setting error message:', errorMessage);
      setError(errorMessage);
    } finally {
      setLoading(false);
      console.log('ðŸ”§ Register Debug - Form submission completed');
    }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-50 py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-md w-full space-y-8">
        <div>
          <h2 className="mt-6 text-center text-3xl font-extrabold text-gray-900">
            Create your account
          </h2>
          <p className="mt-2 text-center text-sm text-gray-600">
            Or{' '}
            <Link
              to="/login"
              className="font-medium text-primary-600 hover:text-primary-500"
            >
              sign in to your existing account
            </Link>
          </p>
        </div>
        
        <form className="mt-8 space-y-6" onSubmit={handleSubmit}>
          {error && (
            <div className="bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded-md">
              {error}
            </div>
          )}
          
          <div className="space-y-4">
            <div>
              <label htmlFor="username" className="block text-sm font-medium text-gray-700">
                Username
              </label>
              <div className="mt-1 relative">
                <input
                  id="username"
                  name="username"
                  type="text"
                  autoComplete="username"
                  required
                  value={username}
                  onChange={(e) => setUsername(e.target.value)}
                  className="input pl-10"
                  placeholder="Enter your username"
                />
                <User className="h-5 w-5 text-gray-400 absolute left-3 top-1/2 transform -translate-y-1/2" />
              </div>
            </div>
            
            <div>
              <label htmlFor="email" className="block text-sm font-medium text-gray-700">
                Email address
              </label>
              <div className="mt-1 relative">
                <input
                  id="email"
                  name="email"
                  type="email"
                  autoComplete="email"
                  required
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  className="input pl-10"
                  placeholder="Enter your email"
                />
                <Mail className="h-5 w-5 text-gray-400 absolute left-3 top-1/2 transform -translate-y-1/2" />
              </div>
            </div>
            
            <div>
              <label htmlFor="password" className="block text-sm font-medium text-gray-700">
                Password
              </label>
              <div className="mt-1 relative">
                <input
                  id="password"
                  name="password"
                  type={showPassword ? 'text' : 'password'}
                  autoComplete="new-password"
                  required
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  className="input pl-10 pr-10"
                  placeholder="Enter your password"
                />
                <Lock className="h-5 w-5 text-gray-400 absolute left-3 top-1/2 transform -translate-y-1/2" />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  className="absolute right-3 top-1/2 transform -translate-y-1/2 text-gray-400 hover:text-gray-600"
                >
                  {showPassword ? <EyeOff className="h-5 w-5" /> : <Eye className="h-5 w-5" />}
                </button>
              </div>
            </div>
          </div>

          <div>
            <button
              type="submit"
              disabled={loading}
              className="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-primary-600 hover:bg-primary-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-primary-500 disabled:opacity-50 disabled:cursor-not-allowed"
            >
              {loading ? 'Creating account...' : 'Create account'}
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default Register; 